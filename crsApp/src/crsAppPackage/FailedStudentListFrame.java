    private void addRowClickListener() {
        jTable_Student_Failed.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                if (evt.getClickCount() == 2) { // double-click
                    int row = jTable_Student_Failed.getSelectedRow();

                    if (row >= 0) {
                        StudentAcademicProfile selectedStudent =
                            failedStudents.get(row);

                        openCourseRecoveryPlan(selectedStudent);
                    }
                }
            }
        });
    }
    
