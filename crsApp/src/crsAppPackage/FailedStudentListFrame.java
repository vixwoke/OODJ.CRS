    private void tfSearchBarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfSearchBarKeyReleased

        String keyword = tfSearchBar.getText().trim().toLowerCase();

        tableModel.setRowCount(0);

        for (StudentAcademicProfile s : failedStudents) {

            String id = s.getStudentID().toLowerCase();
            String name = s.getStudentName().toLowerCase();

            if (id.contains(keyword) || name.contains(keyword)) {
                tableModel.addRow(new Object[]{
                    s.getStudentID(),
                    s.getStudentName(),
                    s.getStudentMajor(),
                    String.format("%.2f", s.calculateCGPA()),
                    s.getStudentYear(),
                    s.getStudentEmail()
                });
            }
        }

    }//GEN-LAST:event_tfSearchBarKeyReleased

